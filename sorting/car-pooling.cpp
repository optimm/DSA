bool carPooling(vector<vector<int>> &v, int c)
{
    vector<pair<int, int>> points;

    for (auto &it : v)
    {
        points.push_back(make_pair(it[1], it[0]));
        points.push_back(make_pair(it[2], -it[0]));
    }
    sort(points.begin(), points.end());
    int cap = 0;
    for (auto it : points)
    {
        cap += it.second;
        if (cap > c)
            return false;
    }
    return true;
}
